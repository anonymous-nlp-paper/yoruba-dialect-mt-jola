"""## Step 4 — Evaluation Metrics"""

import numpy as np
from sacrebleu.metrics import BLEU, CHRF, TER
from bert_score import score as bert_score
from rouge_score import rouge_scorer

def compute_metrics(predictions, references, dialect_name):
    # Filter empty predictions
    pairs = [(p, r) for p, r in zip(predictions, references) if p.strip()]
    preds = [p for p, r in pairs]
    refs  = [r for p, r in pairs]

    print(f'  Evaluating {len(preds)} valid predictions...')

    bleu  = BLEU().corpus_score(preds, [refs]).score
    chrf  = CHRF(word_order=1).corpus_score(preds, [refs]).score
    _, _, F1 = bert_score(preds, refs, lang='en', verbose=False)
    bs_f1 = F1.mean().item()
    scorer = rouge_scorer.RougeScorer(['rougeL'], use_stemmer=True)
    rougeL = np.mean([scorer.score(r, p)['rougeL'].fmeasure for p, r in zip(preds, refs)])
    ter = TER().corpus_score(preds, [refs]).score

    print(f'\n── {dialect_name} Metrics ')
    print(f'  BLEU         : {bleu:.2f}')
    print(f'  chrF+        : {chrf:.2f}')
    print(f'  BERTScore F1 : {bs_f1:.4f}')
    print(f'  ROUGE-L      : {rougeL:.4f}')
    print(f'  TER (↓)      : {ter:.2f}')

    return {
        'Dialect'      : dialect_name,
        'BLEU'         : round(bleu, 2),
        'chrF+'        : round(chrf, 2),
        'BERTScore F1' : round(bs_f1, 4),
        'ROUGE-L'      : round(rougeL, 4),
        'TER (↓)'      : round(ter, 2)
    }

std_m   = compute_metrics(std_preds,   std_refs,   'Std Yoruba')
ilaje_m = compute_metrics(ilaje_preds, ilaje_refs, 'Ilaje')
ife_m   = compute_metrics(ife_preds,   ife_refs,   'Ife')

