# 100 samples per dialect
def get_samples(df, source_col, n=100, seed=42):
    data = []
    for _, row in df.iterrows():
        if pd.notna(row[source_col]) and pd.notna(row['english_text']):
            data.append({
                'source'    : row[source_col],
                'reference' : row['english_text']
            })
    # Fixed random sample for reproducibility
    import random
    random.seed(seed)
    return random.sample(data, min(n, len(data)))

std_data   = get_samples(df, 'std yoruba transcription', n=101)
ilaje_data = get_samples(df, 'Ilaje transcription',      n=101)
ife_data   = get_samples(df, 'Ife transcription',        n=101)

