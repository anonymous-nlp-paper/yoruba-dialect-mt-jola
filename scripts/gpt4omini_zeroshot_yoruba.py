def save_predictions(preds, refs, data, filename):
    rows = []
    for pred, ref, item in zip(preds, refs, data):
        rows.append({
            'source_yoruba'     : item['source'],
            'reference_english' : ref,
            'predicted_english' : pred
        })
    df = pd.DataFrame(rows)
    df.to_csv(filename, index=False)
    print(f'Saved {filename} — {len(df)} rows')
    return df

