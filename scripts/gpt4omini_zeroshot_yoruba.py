"""## Step 3 — GPT-4o-mini Zero-Shot


"""

import time

def run_gpt_inference(client, data, dialect_name):
    predictions, references = [], []
    print(f'\nRunning GPT-4o-mini inference for {dialect_name} ({len(data)} samples)...')

    for i, item in enumerate(data):
        try:

            prompt = (
                f'Translate this from {dialect_name} Yoruba to English.\n'
                f'{dialect_name} Yoruba: {item["source"]}\n'
                f'English:'
            )

            response = client.chat.completions.create(
                model='gpt-4o-mini',
                messages=[
                    {
                        'role': 'system',
                        'content': 'You are a professional translator. Translate the given Yoruba dialect text to English. Output only the English translation, nothing else.'
                    },
                    {
                        'role': 'user',
                        'content': prompt
                    }
                ],
                max_tokens=128,
                temperature=0.0   # deterministic output
            )

            pred = response.choices[0].message.content.strip()
            predictions.append(pred)
            references.append(item['reference'])

            if (i+1) % 10 == 0:
                print(f'  {i+1}/{len(data)} done...')

            # Small delay to avoid rate limiting
            time.sleep(0.5)

        except Exception as e:
            print(f'  Sample {i} failed: {e}')
            predictions.append('')
            references.append(item['reference'])

    print(f'{dialect_name} done — {len(predictions)} predictions')
    return predictions, references

std_preds,   std_refs   = run_gpt_inference(client, std_data,   'Standard')
