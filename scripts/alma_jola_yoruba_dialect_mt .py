# Format each dialect using ALMA official prompt format
def format_data(df, source_col, dialect_name):
    data = []
    for _, row in df.iterrows():
        if pd.notna(row[source_col]) and pd.notna(row['english_text']):
            data.append({
                'instruction': f'Translate this from {dialect_name} Yoruba to English:\n{dialect_name} Yoruba: {row[source_col]}\nEnglish:',
                'answer': row['english_text']
            })
    return data

