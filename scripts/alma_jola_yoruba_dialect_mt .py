def run_inference(model, tokenizer, data, dialect_name):
    predictions, references = [], []
    print(f'\nRunning inference for {dialect_name}...')

    with torch.no_grad():
        for item in data:
            inputs = tokenizer(
                item['instruction'],
                return_tensors='pt',
                truncation=True,
                max_length=512
            ).to(device)

            input_len = inputs['input_ids'].shape[1]

            outputs = model.generate(
                **inputs,
                max_new_tokens=128,
                num_beams=4,
                early_stopping=True,
                no_repeat_ngram_size=3,
                repetition_penalty=1.3,
                pad_token_id=tokenizer.eos_token_id
            )

            # Decode only generated part — skip the instruction
            pred = tokenizer.decode(outputs[0][input_len:], skip_special_tokens=True)
            predictions.append(pred.strip())
            references.append(item['answer'])

    print(f'{dialect_name} — {len(predictions)} samples predicted')
    return predictions, references

def run_inference(model, tokenizer, data, dialect_name):
    predictions, references = [], []
    print(f'\nRunning inference for {dialect_name}...')

    with torch.no_grad():
        for i, item in enumerate(data):
            try:
                inputs = tokenizer(
                    item['instruction'],
                    return_tensors='pt',
                    truncation=True,
                    max_length=256
                ).to(device)

                input_len = inputs['input_ids'].shape[1]

                outputs = model.generate(
                    **inputs,
                    max_new_tokens=64,
                    num_beams=1,
                    do_sample=False,
                    pad_token_id=tokenizer.eos_token_id
                )

                pred = tokenizer.decode(outputs[0][input_len:], skip_special_tokens=True)
                predictions.append(pred.strip())
                references.append(item['answer'])

                if (i+1) % 10 == 0:
                    print(f'  {i+1}/{len(data)} done...')

            except Exception as e:
                print(f'  Sample {i} skipped: {e}')
                predictions.append('')
                references.append(item['answer'])

    print(f'{dialect_name} — {len(predictions)} samples predicted')
    return predictions, references

