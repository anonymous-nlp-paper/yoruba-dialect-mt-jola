import numpy as np
from sacrebleu.metrics import BLEU, CHRF, TER
from bert_score import score as bert_score
from rouge_score import rouge_scorer

def compute_metrics(predictions, references, dialect_name):
    # BLEU
    bleu_score = BLEU().corpus_score(predictions, [references]).score

    # chrF+ (word_order=1)
    chrf_score = CHRF(word_order=1).corpus_score(predictions, [references]).score

    # BERTScore F1
    _, _, F1 = bert_score(predictions, references, lang='en', verbose=False)
    bertscore_f1 = F1.mean().item()

    # ROUGE-L
    scorer = rouge_scorer.RougeScorer(['rougeL'], use_stemmer=True)
    rougeL = np.mean([
        scorer.score(ref, pred)['rougeL'].fmeasure
        for ref, pred in zip(references, predictions)
    ])

    # TER (lower is better)
    ter_score = TER().corpus_score(predictions, [references]).score

    print(f'\n── {dialect_name} Metrics ──────────────────────')
    print(f'  BLEU         : {bleu_score:.2f}')
    print(f'  chrF+        : {chrf_score:.2f}')
    print(f'  BERTScore F1 : {bertscore_f1:.4f}')
    print(f'  ROUGE-L      : {rougeL:.4f}')
    print(f'  TER (↓)      : {ter_score:.2f}')

    return {
        'Dialect'      : dialect_name,
        'BLEU'         : round(bleu_score, 2),
        'chrF+'        : round(chrf_score, 2),
        'BERTScore F1' : round(bertscore_f1, 4),
        'ROUGE-L'      : round(rougeL, 4),
        'TER (↓)'      : round(ter_score, 2)
    }

