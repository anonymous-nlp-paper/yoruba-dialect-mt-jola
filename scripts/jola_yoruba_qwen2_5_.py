def run_inference(model, tokenizer, data, dialect_name):
    predictions, references = [], []
    print(f'\nRunning inference for {dialect_name}...')

    with torch.no_grad():
        for item in data:
            inputs = tokenizer(
                item['instruction'],
                return_tensors='pt',
                truncation=True,
                max_length=512
            ).to(device)

            input_len = inputs['input_ids'].shape[1]

            outputs = model.generate(
                **inputs,
                max_new_tokens=128,
                num_beams=4,
                early_stopping=True
            )

            # Decode only the generated part (skip the instruction)
            pred = tokenizer.decode(outputs[0][input_len:], skip_special_tokens=True)
            predictions.append(pred.strip())
            references.append(item['answer'])

    print(f'{dialect_name} — {len(predictions)} samples predicted')
    return predictions, references

